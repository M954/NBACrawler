def expand_twitter_slang(text: str) -> str:
    """使用单词边界匹配展开 Twitter 口语/缩写。"""
    import re
    # 统计术语缩写：仅匹配全大写形式（TO, W, L 等），避免破坏常见英文单词
    CASE_SENSITIVE_KEYS = {"TO", "W", "L", "FG", "FT", "GP", "MIN"}
    for slang, expanded in TWITTER_SLANG_EXPAND.items():
        pattern = r'\b' + re.escape(slang) + r'\b'
        if slang in CASE_SENSITIVE_KEYS:
            # 只匹配全大写形式
            text = re.sub(pattern, expanded, text)
        else:
            text = re.sub(pattern, expanded, text, flags=re.IGNORECASE)
    return text
